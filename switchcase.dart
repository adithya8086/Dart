void main() {
  var month = 12;

  switch(month) {
    case 1:
       print('month is january');
       break;

    case 2:
      print('month is feb');
      break;

    case 3:
      print('month is may');
      break;

    case 4:
      print('month is april');
      break;

    default:
      print("none of these");
  }
}